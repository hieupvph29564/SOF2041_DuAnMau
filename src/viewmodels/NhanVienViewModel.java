package viewmodels;

/**
 *
 * @author virus
 */
public class NhanVienViewModel {

    private String ma;
    private String ho;
    private String tenDem;
    private String ten;
    private String gioiTinh;
    private String diaChi;
    private int trangThai;

    public NhanVienViewModel() {
    }

    public NhanVienViewModel(String ma, String ho, String tenDem, String ten, String gioiTinh, String diaChi, int trangThai) {
        this.ma = ma;
        this.ho = ho;
        this.tenDem = tenDem;
        this.ten = ten;
        this.gioiTinh = gioiTinh;
        this.diaChi = diaChi;
        this.trangThai = trangThai;
    }

    public int getTrangThai() {
        return trangThai;
    }

    public void setTrangThai(int trangThai) {
        this.trangThai = trangThai;
    }

    public String getMa() {
        return ma;
    }

    public void setMa(String ma) {
        this.ma = ma;
    }

    public String getHo() {
        return ho;
    }

    public void setHo(String ho) {
        this.ho = ho;
    }

    public String getTenDem() {
        return tenDem;
    }

    public void setTenDem(String tenDem) {
        this.tenDem = tenDem;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getGioiTinh() {
        return gioiTinh;
    }

    public void setGioiTinh(String gioiTinh) {
        this.gioiTinh = gioiTinh;
    }

    public String getDiaChi() {
        return diaChi;
    }

    public void setDiaChi(String diaChi) {
        this.diaChi = diaChi;
    }

    public Object[] toRowData() {
        return new Object[]{this.ma, (this.ho + " " + this.tenDem + " " + this.ten), this.gioiTinh, this.diaChi, this.trangThai};
    }
}
