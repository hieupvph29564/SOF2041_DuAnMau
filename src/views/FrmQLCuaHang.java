    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        CuaHang ch = new CuaHang();
        ch.setMa(txtMa.getText());
        ch.setTen(txtTen.getText());
        ch.setDiaChi(txtDiaChi.getText());
        ch.setThanhPho(txtThanhPho.getText());
        ch.setQuocGia(txtQuocGia.getText());
        JOptionPane.showMessageDialog(rootPane, service.addCuaHang(ch));
        showDataTable(service.getAllCuaHang());
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int index = tbQLCH.getSelectedRow();
        String ma = tbQLCH.getValueAt(index, 1).toString();
        CuaHang ch = new CuaHang();
        ch.setMa(txtMa.getText());
        ch.setTen(txtTen.getText());
        ch.setDiaChi(txtDiaChi.getText());
        ch.setThanhPho(txtThanhPho.getText());
        ch.setQuocGia(txtQuocGia.getText());
        JOptionPane.showMessageDialog(rootPane, service.updateCuaHang(ch, ma));
        showDataTable(service.getAllCuaHang());
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int index = tbQLCH.getSelectedRow();
        String ma = tbQLCH.getValueAt(index, 1).toString();
        JOptionPane.showMessageDialog(rootPane, service.deleteCuaHang(ma));
        showDataTable(service.getAllCuaHang());
    }//GEN-LAST:event_btnDeleteActionPerformed

