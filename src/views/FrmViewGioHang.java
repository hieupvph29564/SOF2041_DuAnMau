    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        String ma = jTable1.getValueAt(index, 0).toString();
        GioHangViewModel gh = new GioHangViewModel(txtma.getText(),
                cbbIDKH.getSelectedItem().toString(), txtTen.getText(),
                jComboBox1.getSelectedIndex());
        JOptionPane.showMessageDialog(rootPane, service.updateGioHang(gh, ma));
        showDataTable(service.getAllGioHang());
    }//GEN-LAST:event_jButton2ActionPerformed

