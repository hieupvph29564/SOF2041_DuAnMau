    private void showDataComboBox(List<String> list) {
        for (String s : list) {
            dcbm.addElement(s);
        }
    }

    private void showDataTable(List<GioHangViewModel> list) {
        dtm.setRowCount(0);
        for (GioHangViewModel gh : list) {
            dtm.addRow(gh.toRowData());
        }
    }

